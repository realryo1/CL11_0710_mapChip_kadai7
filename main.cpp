//==================================================
//マップスクロール関数
//==================================================
void mapScroll(void)
{
	//Y・縦

	//プレイヤー中心に来るまで
	if (bigPosY <= SCROLLSTOP_Y)
	{
		scroll_Y = 0;
	}
	//プレイヤー中心
	else if (bigPosY > SCROLLSTOP_Y && bigPosY - SCROLLSTOP_Y < BIGMAP_Y - MAP_Y)
	{
		scroll_Y = bigPosY - SCROLLSTOP_Y;
	}
	//プレイヤー下端に到達
	else
	{
		scroll_Y = BIGMAP_Y - MAP_Y;
	}

	//X・横

	//プレイヤー中心に来るまで
	if (bigPosX <= SCROLLSTOP_X)
	{
		scroll_X = 0;
	}
	//プレイヤー中心
	else if (bigPosX > SCROLLSTOP_X && bigPosX - SCROLLSTOP_X < BIGMAP_X - MAP_X)
	{
		scroll_X = bigPosX - SCROLLSTOP_X;
	}
	//プレイヤー右端に到達
	else
	{
		scroll_X = BIGMAP_X - MAP_X;
	}
}

//==================================================
//マップ初期化関数
//==================================================
void initializeMap(void)
{
	//描画マップ設定
	for (int y = 0; y < MAP_Y; y++)
	{
		for (int x = 0; x < MAP_X; x++)
		{
			map[y][x] = baseBigMap[scroll_Y + y][scroll_X + x];


			if (map[y][0] == -1)//横スクロール時右端全角消滅時のずれを修正
			{
				map[y][0] = 0;
			}
			else if (map[y][MAP_X - 1] >= 9 && map[y][MAP_X - 1] <= 98)//横スクロール時左端全角存在時非表示。9～98のマーカーは全角なため
			{
				map[y][MAP_X - 1] = 0;
			}
		}
	}
}

//==================================================
//プレイヤー位置設定関数
//==================================================
void playerPosSet(void)
{
	if (baseBigMap[bigPosY][bigPosX] != -1)
	{
		if (baseBigMap[bigPosY][bigPosX] >= 9 && baseBigMap[bigPosY][bigPosX] <= 98)//9～98のマーカーは全角なため
		{
			map[bigPosY - scroll_Y][bigPosX - scroll_X + 1] = 0;//全角重なり時ずれ回避用半角スペース挿入
		}
		map[bigPosY - scroll_Y][bigPosX - scroll_X] = 99;//プレイヤー位置指定
	}
}

